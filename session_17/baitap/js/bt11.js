// Sử dụng hàm Math.abs()
console.log(Math.abs(-5.5)); // !5.5 - Hàm Math.abs() trả về giá trị tuyệt đối của số truyền vào.
console.log(Math.abs(10)); // !10 - Hàm Math.abs() trả về giá trị tuyệt đối của số truyền vào.

// Sử dụng hàm Math.ceil()
console.log(Math.ceil(4.4)); // !5 - Hàm Math.ceil() làm tròn số lên đến số nguyên gần nhất.
console.log(Math.ceil(5.8)); // !6 - Hàm Math.ceil() làm tròn số lên đến số nguyên gần nhất.

// Sử dụng hàm Math.floor()
console.log(Math.floor(5.7)); // !5 - Hàm Math.floor() làm tròn số xuống đến số nguyên gần nhất.
console.log(Math.floor(6.1)); // !6 - Hàm Math.floor() làm tròn số xuống đến số nguyên gần nhất.

// Sử dụng hàm Math.round()
console.log(Math.round(10.1)); // !10 - Hàm Math.round() làm tròn số đến số nguyên gần nhất.
console.log(Math.round(20.6)); // !21 - Hàm Math.round() làm tròn số đến số nguyên gần nhất.