let n = Number(prompt("nhap so bat ki"));

switch(n){
    case 0:
        document.write("so khong");
        break;
    case 1:
        document.write("so mot");
        break;
    case 2:
        document.write("so hai");
        break;
    case 3:
        document.write("so ba");
        break;
    case 4:
        document.write("so bon");
        break;
    default:
        document.write("khong ton tai");
}